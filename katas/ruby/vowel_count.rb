require 'minitest/autorun'

class TestVowelCount < Minitest::Test
  def get_count(str)
    str.chars.find_all { |char| char =~ /[aiueo]/i}.length
  end

  def test_vowel_count_empty_string
    assert_equal(0, get_count(''))
  end

  def test_vowel_count_word
    assert_equal(5, get_count('abracadabra'))
  end

  def test_vowel_count_two_words
    assert_equal(4, get_count('pear tree'))
  end

  def test_vowel_count_random
    assert_equal(13, get_count('o a kak ushakov lil vo kashu kakao'))
  end

